// for-loop

// log to console numbers from 1 to 10
for (let i=1;i<10;i++) {
    console.log(i);
}

// looping through a list
desserts = ['carrot cake', 'kitkat', 'ice cream', 'apple pie']
for  (let i=0;i<desserts.length;i++) {
    console.log(desserts[i])
}

for (const dessert of desserts) {
    console.log(dessert)
}

